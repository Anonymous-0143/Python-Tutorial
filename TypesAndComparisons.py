# We can use comparisons to check if a number is less than or greater than another number.
print(1 < 2) # Output:- True
print(1 > 1) # Output:- False


# To check if a number is less than or equal to another number, we use less-than-or-equal-to operator, <= and vice-versa, >=
print(1 <= 3) # Output:- True
print(34 >= 34) # Output:- True


# We can use comparisons to check if a string is equal to or not equal to another string
print("online" == "online") # Output:- True
print("online" != "offline") # Output:- True


